        ]);

        return back()->with('success', 'ƒê√°nh gi√° ƒë√£ ƒë∆∞·ª£c ghi nh·∫≠n!');
    }



    public function huyDon($id)
    {
        $donHang = DonHang::with('chiTietDonHangs.chiTietSanPham')
            ->where('id_khach_hang', Auth::user()->khachHang->id)
            ->where('trang_thai', '!=', 'ƒê√£ duy·ªát')
            ->where('trang_thai', '!=', 'Ho√†n th√†nh')
            ->where('trang_thai', '!=', 'H·ªßy')
            ->findOrFail($id);

        try {
            DB::beginTransaction();

            // Tr·∫£ l·∫°i t·ªìn kho cho t·ª´ng chi ti·∫øt s·∫£n ph·∫©m
