            ->where('chi_tiet_san_pham.id_san_pham', $request->id_san_pham)
            ->exists();

        if (!$sanPhamTonTai) {
            return back()->with('error', 'S·∫£n ph·∫©m kh√¥ng t·ªìn t·∫°i trong ƒë∆°n h√†ng.');
        }

        $daDanhGia = DanhGia::where('id_khach_hang', $khachHang->id)
