                    $ctsp->increment('so_luong', $ct->so_luong);
                }
            }

            // C·∫≠p nh·∫≠t tr·∫°ng th√°i ƒë∆°n
            $donHang->update(['trang_thai' => 'H·ªßy']);

            DB::commit();
