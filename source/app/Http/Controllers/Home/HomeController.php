    public function placeOrder(Request $request)
    {
        if (!Auth::check()) {
            return redirect()->route('login')->with('error', 'Vui l√≤ng ƒëƒÉng nh·∫≠p ƒë·ªÉ ƒë·∫∑t h√†ng.');
        }

        $request->validate([
            'ten_nguoi_nhan' => 'required|string|max:255',
            'dia_chi_nhan' => 'required|string|max:500',
            'sdt' => 'required|string|max:500',

        ]);

        $cart = session('cart', []);
        if (empty($cart)) {
            return back()->with('error', 'Gi·ªè h√†ng tr·ªëng.');
        }

        $tongTien = 0;

        foreach ($cart as $idChiTiet => $item) {
            $tongTien += $item['gia'] * $item['so_luong'];
        }

        // T·∫°o ƒë∆°n h√†ng
        $donHang = DonHang::create([
            'ma' => strtoupper(Str::random(8)),
            'id_khach_hang' => Auth::user()->khachHang->id,
            'ten_nguoi_nhan' => $request->ten_nguoi_nhan,
            'dia_chi_nhan' => $request->dia_chi_nhan,
            'sdt' => $request->sdt,
            'ngay_dat' => Carbon::now(),
            'tong_tien' => $tongTien,
            'trang_thai' => 'Ch·ªù duy·ªát',
            'thanh_toan' => $request->thanh_toan,
        ]);

        // L∆∞u chi ti·∫øt ƒë∆°n h√†ng
        foreach ($cart as $idChiTiet => $item) {
            ChiTietDonHang::create([
                'id_don_hang' => $donHang->id,
                'id_chi_tiet_san_pham' => $idChiTiet,
                'so_luong' => $item['so_luong'],
                'gia' => $item['gia'],
            ]);

            // Tr·ª´ t·ªìn kho
            $chiTiet = ChiTietSanPham::find($idChiTiet);
            $chiTiet->so_luong -= $item['so_luong'];
            $chiTiet->save();
        }

        // X√≥a gi·ªè h√†ng
        session()->forget('cart');

        return redirect()->route('home.index')->with('success', 'ƒê·∫∑t h√†ng th√†nh c√¥ng!');
    }

    public function updateQuantity(Request $request)
    {
        $request->validate([
            'id' => 'required',
            'action' => 'required|in:plus,minus'
        ]);

        $cart = session('cart', []);
        $id = $request->id;

        if (!isset($cart[$id])) {
            return response()->json(['error' => 'S·∫£n ph·∫©m kh√¥ng t·ªìn t·∫°i'], 404);
        }

        $chiTiet = \App\Models\ChiTietSanPham::with('sanPham')->find($id);
        if (!$chiTiet) {
            return response()->json(['error' => 'Kh√¥ng t√¨m th·∫•y chi ti·∫øt s·∫£n ph·∫©m'], 404);
        }

        $hienTai = $cart[$id]['so_luong'];

        if ($request->action == 'plus') {
            if ($hienTai < $chiTiet->so_luong) {
                $cart[$id]['so_luong'] += 1;
            } else {
                return response()->json(['error' => 'Trong kho kh√¥ng ƒë·ªß h√†ng'], 400);
            }
        } elseif ($request->action == 'minus') {
            $cart[$id]['so_luong'] -= 1;
            if ($cart[$id]['so_luong'] <= 0) {
                unset($cart[$id]);
            }
        }

        session()->put('cart', $cart);
        return response()->json(['success' => true]);
    }


